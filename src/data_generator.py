# Модуль для генерации синтетических данных датчиков
# Имитирует показания оборудования (температура, вибрация, напряжение)

# ============================================================================
#                                 ИМПОРТЫ
# ============================================================================

import pandas as pd      # Работа с таблицами данных
import numpy as np       # Математические операции и генерация случайных чисел
from datetime import datetime, timedelta  # Работа с датами и временем
from pathlib import Path   # Удобная работа с путями к файлам

# ============================================================================
#                        ФУНКЦИЯ 1: ГЕНЕРАЦИЯ ДАННЫХ
# ============================================================================

def generate_data(n_points=1000, anomaly_ratio=0.05, random_seed=42):
    """
    Генерирует синтетические данные для симуляции датчиков оборудования.
    
    Параметры:
        n_points (int): Количество записей для генерации (по умолчанию 1000)
        anomaly_ratio (float): Доля аномальных записей (0.05 = 5%)
        random_seed (int): Фиксированное число для воспроизводимости результатов
    
    Возвращает:
        pd.DataFrame: Таблица с колонками:
            - timestamp: время показания
            - device_id: идентификатор устройства
            - temperature: температура (°C)
            - vibration: вибрация (мм/с)
            - voltage: напряжение (В)
            - is_anomaly_real: метка "была ли это реальная аномалия" (0 или 1)
    """
    
    # Фиксируем случайность — чтобы при каждом запуске данные были одинаковыми
    # Это важно для отладки и тестов
    np.random.seed(random_seed)
    
    # ------------------------------------------------------------------------
    #                   ШАГ 1: Создаём временную шкалу
    # ------------------------------------------------------------------------
    # Генерируем даты: от текущего момента назад на n_points минут
    start_time = datetime.now()
    timestamps = [start_time - timedelta(minutes=i) for i in range(n_points)]
    
    # Переворачиваем список, чтобы время шло от прошлого к будущему
    timestamps.reverse()
    
    # ------------------------------------------------------------------------
    #            ШАГ 2: Генерируем "нормальные" показатели датчиков
    # ------------------------------------------------------------------------
    # Температура: в норме 45°C с небольшими колебаниями (±2°C)
    # np.random.normal(среднее, стандартное_отклонение, размер)
    temperature = np.random.normal(loc=45.0, scale=2.0, size=n_points)
    
    # Вибрация: в норме 0.2 мм/с с небольшими колебаниями
    vibration = np.random.normal(loc=0.2, scale=0.05, size=n_points)
    
    # Напряжение: в норме 220В с небольшими скачками
    voltage = np.random.normal(loc=220.0, scale=3.0, size=n_points)
    
    # ------------------------------------------------------------------------
    #                        ШАГ 3: Добавляем аномалии
    # ------------------------------------------------------------------------
    # Выбираем случайные индексы для аномалий (5% от всех данных)
    n_anomalies = int(n_points * anomaly_ratio)
    anomaly_indices = np.random.choice(n_points, size=n_anomalies, replace=False)
    
    # Для выбранных индексов меняем значения на "аварийные"
    # Температура резко растёт (70-90°C вместо 45°C)
    temperature[anomaly_indices] = np.random.uniform(70.0, 90.0, size=n_anomalies)
    
    # Вибрация увеличивается (0.8-1.5 мм/с вместо 0.2 мм/с)
    vibration[anomaly_indices] = np.random.uniform(0.8, 1.5, size=n_anomalies)
    
    # Напряжение скачет (180-190В или 250-260В вместо 220В)
    # Для каждой аномалии случайно выбираем: низкое или высокое напряжение
    voltage_choices = np.random.choice([0, 1], size=n_anomalies)  # 0 = низкое, 1 = высокое
    voltage[anomaly_indices] = np.where(
        voltage_choices == 0,
        np.random.uniform(180, 190, size=n_anomalies),   # Низкое напряжение
        np.random.uniform(250, 260, size=n_anomalies)    # Высокое напряжение
    )
    
    # ------------------------------------------------------------------------
    #                 ШАГ 4: Собираем всё в таблицу (DataFrame)
    # ------------------------------------------------------------------------
    df = pd.DataFrame({
        'timestamp': timestamps,
        'device_id': 'pump_01',  # ID оборудования (потом можно добавить больше)
        'temperature': temperature,
        'vibration': vibration,
        'voltage': voltage,
        'is_anomaly_real': 0  # По умолчанию все записи считаем нормальными
    })
    
    # Помечаем аномальные записи единицей (1 = аномалия)
    df.loc[anomaly_indices, 'is_anomaly_real'] = 1
    
    # Возвращаем готовую таблицу
    return df


# ============================================================================
#                      ФУНКЦИЯ 2: СОХРАНЕНИЕ В CSV
# ============================================================================

def save_to_csv(df, filepath):
    """
    Сохраняет DataFrame в CSV-файл.
    
    Параметры:
        df (pd.DataFrame): Таблица с данными для сохранения
        filepath (Path или str): Путь к файлу (например, "data/raw_data.csv")
    
    Возвращает:
        None
    """
    
    # Создаём папку, если она не существует
    # parent = родительская папка файла, exist_ok = не ошибка если папка уже есть
    Path(filepath).parent.mkdir(parents=True, exist_ok=True)
    
    # Сохраняем в CSV
    # index=False — не сохраняем номер строки как отдельный столбец
    df.to_csv(filepath, index=False, encoding='utf-8')
    
    # Примечание: файл data/*.csv добавлен в .gitignore, 
    # поэтому он не попадёт в GitHub (защита от лишних файлов)


