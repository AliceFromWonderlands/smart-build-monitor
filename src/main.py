# Стандартные библиотеки Python
import sys  # Нужен для корректного завершения программы при ошибке
from pathlib import Path  # Удобная работа с путями к файлам

# Импорт функций из своих модулей (создам потом и пототм наполню)
from src.data_generator import generate_data, save_to_csv
from src.anomaly_detector import load_data, detect_anomalies, print_results
from src.visualizer import plot_and_save

# ============================================================================
#                            НАСТРОЙКИ ПРОЕКТА
# ============================================================================

# Словарь с конфигурацией
CONFIG = {
    "data_dir": Path("data"),           # Папка для данных
    "filename": "raw_data.csv",         # Имя файла с данными
    "n_points": 1000,                   # Сколько точек данных генерировать
    "anomaly_ratio": 0.05,              # Доля аномалий (5%)
    "random_seed": 42,                  # Фиксация случайности для воспроизводимости
}

# ============================================================================
#                             ГЛАВНАЯ ФУНКЦИЯ
# ============================================================================

def main():
    """
    Основная функция программы.
    Запускает весь пайплайн: генерация → анализ → визуализация.
    """
    # Блок try-except для отлова ошибок — чтобы программа не падала молча
    try:
        print("🚀 Запуск SmartBuild Monitor MVP...")
        print("-" * 50)
        
        # --------------------------------------------------------------------
        #                      ШАГ 1: Генерация данных
        # --------------------------------------------------------------------
        print("📊 Шаг 1: Генерация синтетических данных...")
        
        # Вызываем функцию из data_generator.py
        # Она вернет DataFrame с данными
        df = generate_data(
            n_points=CONFIG["n_points"],
            anomaly_ratio=CONFIG["anomaly_ratio"],
            random_seed=CONFIG["random_seed"]
        )
        
        # Простая проверка: если данных нет — останавливаемся
        if df is None or df.empty:
            raise ValueError("❌ Не удалось сгенерировать данные. Проверь data_generator.py")
        
        print(f"   ✅ Сгенерировано {len(df)} записей")
        
        # Сохраняем данные в CSV (чтобы потом можно было посмотреть в Excel)
        output_path = CONFIG["data_dir"] / CONFIG["filename"]
        save_to_csv(df, output_path)
        print(f"   💾 Данные сохранены в {output_path}")
        
        # --------------------------------------------------------------------
        #             ШАГ 2: Обнаружение аномалий (ML-часть)
        # --------------------------------------------------------------------
        print("\n🔍 Шаг 2: Запуск детектора аномалий...")
        
        # Загружаем данные обратно (на практике это может быть чтение из БД)
        df_loaded = load_data(output_path)
        
        # Запускаем модель машинного обучения
        # Функция вернет тот же DataFrame, но с новым столбцом 'is_anomaly_pred'
        df_analyzed = detect_anomalies(df_loaded)
        
        # Печатаем краткую статистику в консоль
        print_results(df_analyzed)
        
        # --------------------------------------------------------------------
        #                ШАГ 3: Визуализация результатов
        # --------------------------------------------------------------------
        print("\n📈 Шаг 3: Построение графика...")
        
        # Строим график и сохраняем как картинку
        plot_path = CONFIG["data_dir"] / "report.png"
        plot_and_save(df_analyzed, plot_path)
        print(f"   🖼️  График сохранён в {plot_path}")
        # --------------------------------------------------------------------
        #                             ФИНАЛ
        # --------------------------------------------------------------------
        print("\n" + "-" * 50)
        print("✨ Все этапы завершены успешно!")
        print(f"📁 Проверь папку '{CONFIG['data_dir']}' с результатами")
    except FileNotFoundError as e:
        # Ошибка: файл не найден
        print(f"❌ Ошибка: файл не найден — {e}")
        sys.exit(1)
    except ValueError as e:
        # Ошибка: некорректные данные
        print(f"❌ Ошибка в данных: {e}")
        sys.exit(1)
    except Exception as e:
        # Любая другая непредвиденная ошибка
        print(f"❌ Неизвестная ошибка: {type(e).__name__} — {e}")
        sys.exit(1)
        
# ============================================================================
#                            ЗАПУСК ПРОГРАММЫ
# ============================================================================

# только прямой запуск
if __name__ == "__main__":
    main()