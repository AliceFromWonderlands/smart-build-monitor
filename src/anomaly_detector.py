# Модуль для обнаружения аномалий в данных датчиков
# Использует алгоритм Isolation Forest из библиотеки scikit-learn

# ============================================================================
#                                ИМПОРТЫ
# ============================================================================

import pandas as pd      # Работа с таблицами данных
import numpy as np       # Математические операции
from sklearn.ensemble import IsolationForest  # ML-алгоритм для поиска аномалий

# ============================================================================
#                    ФУНКЦИЯ 1: ЗАГРУЗКА ДАННЫХ ИЗ CSV
# ============================================================================

def load_data(filepath):
    """
    Загружает данные из CSV-файла в DataFrame.
    
    Параметры:
        filepath (str или Path): Путь к файлу (например, "data/raw_data.csv")
    
    Возвращает:
        pd.DataFrame: Таблица с данными из файла
    
    Примечание:
        Функция автоматически определяет формат даты в колонке 'timestamp'
    """
    #Читаем CSV-файл
    # parse_dates=['timestamp'] — автоматически преобразует текст в даты
    df = pd.read_csv(filepath, parse_dates=['timestamp'])
    
    print(f"   📂 Загружено {len(df)} записей из {filepath}")
    
    return df

# ============================================================================
#                 ФУНКЦИЯ 2: ОБНАРУЖЕНИЕ АНОМАЛИЙ (ML-ЧАСТЬ)
# ============================================================================

def detect_anomalies(df, contamination=0.05, random_seed=42):
    """
    Применяет модель машинного обучения для поиска аномалий в данных.
    
    Параметры:
        df (pd.DataFrame): Таблица с данными для анализа
        contamination (float): Ожидаемая доля аномалий в данных (0.05 = 5%)
        random_seed (int): Фиксированное число для воспроизводимости результатов
    Возвращает:
        pd.DataFrame: Исходная таблица + новая колонка 'is_anomaly_pred'
            - 1 = предсказанная аномалия
            - 0 = норма
    
    Как работает Isolation Forest:
        1. Алгоритм строит множество "деревьев решений"
        2. Аномальные точки легче "изолировать" (отделить от остальных)
        3. Точки, которые изолируются быстрее — считаются аномалиями
    """
    
    # ------------------------------------------------------------------------
    #                 ШАГ 1: Выбираем признаки для анализа
    # ------------------------------------------------------------------------
    
    # Модель будет искать аномалии по этим колонкам
    feature_columns = ['temperature', 'vibration', 'voltage']
    
    # Проверяем, что все колонки существуют в данных
    for col in feature_columns:
        if col not in df.columns:
            raise ValueError(f"❌ Колонка '{col}' не найдена в данных!")
    
    # Извлекаем только нужные колонки для модели
    features = df[feature_columns]
    
    # ------------------------------------------------------------------------
    #                     ШАГ 2: Создаём и обучаем модель
    # ------------------------------------------------------------------------
    
    # IsolationForest — алгоритм для поиска аномалий без учителя
    # contamination — сколько % аномалий мы ожидаем (помогает модели настроить пороги)
    model = IsolationForest(
        contamination=contamination,
        random_state=random_seed,
        n_estimators=20,   # Количество деревьев в "лесу" ОЧЕНЬ МАЛОЕ ДЛЯ ПЕРВЫХ ТЕСТОВ
        n_jobs=-1         # Используем все ядра процессора, использовать на устройстве для расчетов работы программы
    )
    
    # Обучаем модель на наших данных (fit) и сразу предсказываем (predict)
    # predict возвращает: 1 = норма, -1 = аномалия
    predictions = model.fit_predict(features)
    
    # ------------------------------------------------------------------------
    #                 ШАГ 3: Добавляем результаты в таблицу
    # ------------------------------------------------------------------------
    
    # Создаём новую колонку: 1 = аномалия, 0 = норма
    # Преобразуем: -1 → 1, 1 → 0
    df['is_anomaly_pred'] = (predictions == -1).astype(int)
    
    print(f"   🤖 Модель обучена и применила предсказания")
    
    return df

# ============================================================================
#                   ФУНКЦИЯ 3: ВЫВОД РЕЗУЛЬТАТОВ В КОНСОЛЬ
# ============================================================================
def print_results(df):
    """
    Выводит статистику по найденным аномалиям в консоль.
    
    Параметры:
        df (pd.DataFrame): Таблица с данными и предсказаниями
    
    Возвращает:
        None
    """
    
    # Считаем количество аномалий
    total_records = len(df)
    anomaly_count = df['is_anomaly_pred'].sum()
    anomaly_percent = (anomaly_count / total_records) * 100
    
    # Считаем количество реальных аномалий (если есть метка is_anomaly_real)
    if 'is_anomaly_real' in df.columns:
        real_anomaly_count = df['is_anomaly_real'].sum()
        
        # Считаем точность: сколько предсказанных аномалий совпали с реальными
        true_positives = df[
            (df['is_anomaly_pred'] == 1) & 
            (df['is_anomaly_real'] == 1)
        ].shape[0]
        
        # Точность (Precision): сколько из предсказанных аномалий были реальными
        precision = (true_positives / anomaly_count * 100) if anomaly_count > 0 else 0
        
        # Полнота (Recall): сколько реальных аномалий мы нашли
        recall = (true_positives / real_anomaly_count * 100) if real_anomaly_count > 0 else 0
        
        print(f"\n📊 СТАТИСТИКА МОДЕЛИ:")
        print(f"   Всего записей: {total_records}")
        print(f"   Реальных аномалий: {real_anomaly_count}")
        print(f"   Предсказано аномалий: {anomaly_count} ({anomaly_percent:.1f}%)")
        print(f"   ✅ Точность (Precision): {precision:.1f}%")
        print(f"   ✅ Полнота (Recall): {recall:.1f}%")
    else:
        # Если нет меток реальных аномалий — показываем только предсказания
        print(f"\n📊 СТАТИСТИКА МОДЕЛИ:")
        print(f"   Всего записей: {total_records}")
        print(f"   Предсказано аномалий: {anomaly_count} ({anomaly_percent:.1f}%)")
    
    # Показываем пример найденных аномалий (первые 5)
    anomalies = df[df['is_anomaly_pred'] == 1]
    if len(anomalies) > 0:
        print(f"\n⚠️  Примеры найденных аномалий (первые 5):")
        print(anomalies[['timestamp', 'temperature', 'vibration', 'voltage']].head())
    else:
        print("\n✅ Аномалий не найдено")